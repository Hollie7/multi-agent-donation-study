export function getBotLogo(senderName, botProfiles) {
  if (senderName === "Host") {
    return hostlogo;
  }
  if (!botProfiles || Object.keys(botProfiles).length === 0) {
    return hostlogo;
  }
  const botNumber = senderName;
  const profileData = botProfiles[senderName];

  let age_group_name = "";
  if (profileData.ageRange >= 18 && profileData.ageRange <= 30) {
    age_group_name = "1830";
  } else if (profileData.ageRange >= 31 && profileData.ageRange <= 50) {
    age_group_name = "3150";
  } else if (profileData.ageRange > 50) {
    age_group_name = "mo51";
  }

  let gender_name = "";
  if (profileData.gender === "Male") gender_name = "Ma";
  else if (profileData.gender === "Female") gender_name = "Fe";
  else if (profileData.gender === "Non-binary") gender_name = "Nb";

  let ethnicity_name = "";
  if (profileData.ethnicity === "European/White") ethnicity_name = "Wh";
  else if (profileData.ethnicity === "Black") ethnicity_name = "Bl";
  else if (profileData.ethnicity === "Asian") ethnicity_name = "Br";
  else if (profileData.ethnicity === "Indigenous") ethnicity_name = "DB";

  const imageKey = `${botNumber}_${age_group_name}_${gender_name}_${ethnicity_name}`;
  const image = botImageMap[imageKey];

  return image || hostlogo;
}
